const PersonalInformationsService = require("../services/personal_informations");
const { getLocalizedMessage } = require("../utils/Helper");

class PersonalInformationsCtrl {
  static async detail(req, res) {
    try {
      const detail = await PersonalInformationsService.detail(
        req.params.personalInformationsId,
        req.account,
        { lang: req.lang_key }
      );
      return res.status(detail.code).json({
        ...detail,
        message: getLocalizedMessage(
          "personal_informations.detail",
          detail.message,
          req.lang_key
        ),
      });
    } catch (error) {
      return res.status(500).json({
        error,
        severity: "error",
        message: getLocalizedMessage("error", "general", req.lang_key),
      });
    }
  }

  static async update(req, res) {
    try {
      const update = await PersonalInformationsService.update(
        req.params.personalInformationsId,
        req.body,
        req.account,
        { lang: req.lang_key }
      );
      return res.status(update.code).json({
        ...update,
        message: getLocalizedMessage(
          "personal_informations.update",
          update.message,
          req.lang_key
        ),
      });
    } catch (error) {
      return res.status(500).json({
        error,
        severity: "error",
        message: getLocalizedMessage("error", "general", req.lang_key),
      });
    }
  }
}

module.exports = PersonalInformationsCtrl;
